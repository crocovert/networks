        t_links.append(QgsField("longueur",QVariant.Double))
        t_links.append(QgsField("line",QVariant.String))
        t_links.append(QgsField("hdw_monfri",QVariant.Double))
        t_links.append(QgsField("hdw_sat",QVariant.Double))
