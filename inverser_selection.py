        res = self.parameterAsVectorLayer(parameters, self.RESEAU, context)
        if res.selectedFeatureCount()>0:
            features=[i for i in res.selectedFeatures()]
            res.startEditing()
            for f in features:
                geom = f.geometry()
                geom.convertToMultiType()
                nodes = geom.asMultiPolyline()
                nodes.reverse()
                for points in nodes:
                    points.reverse() 
                newgeom = QgsGeometry.fromMultiPolylineXY(nodes)
                i=f.id()
                f.setGeometry(newgeom)
                res.updateFeature(f)
                #res.changeGeometry(i,newgeom)
            res.endEditCommand()
